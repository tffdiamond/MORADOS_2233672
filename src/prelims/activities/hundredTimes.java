public class hundredTimes {
    public static void main(String[] args) {
        int i = 0;
        while (i < 100) {
            System.out.println("Lou Diamond T. Morados");
            i++;
        }
        do {
            System.out.println("Lou Diamond T. Morados");
            i++;
        }
        while (i < 100);
        for (i=0; i<100; i++) System.out.println("Lou Diamond T. Morados");
    }
}